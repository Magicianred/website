module V2ETL
  module DataProcessors
    class ProcessTracks
      include Mandate

      def call
        fix_uuids!
        sync_tracks!
      end

      def fix_uuids!
        UUIDS.each do |slug, old, new|
          track = Track.find_by_slug(slug)
          next unless track

          track.exercises.where(uuid: old).update_all(uuid: new)
        end
      end
        
      def sync_tracks!
        Track.find_each do |track|
          puts "Updating #{track.slug}"
          Git::SyncTrack.(track)
        rescue StandardError
        end
      end

